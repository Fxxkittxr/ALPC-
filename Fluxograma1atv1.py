'''1)	Faça um algoritmo que solicite o número de horas que um trabalhador realiza por dia,
solicite o número de dias trabalhados em um mês e apresente o número total de horas trabalhadas no mês.
'''
print ("Insira o numero de horas trabalhadas por dia:")
horastrab = int(input()) 
print ("Insira o numero de dias trabalhados:")
diastrab = int(input())
numtothoras = diastrab * horastrab
print ("Total de horas trabalhadas é igual à ", numtothoras)
